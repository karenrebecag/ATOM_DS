// ── URL Parsing ─────────────────────────────────────────────────────

function parseFileKey(input: string): string {
  if (/^[a-zA-Z0-9]{10,}$/.test(input)) return input

  const match = input.match(/figma\.com\/(?:design|file)\/([a-zA-Z0-9]+)/)
  if (match) return match[1]

  throw new Error(
    `Could not extract file key from: ${input}\n` +
    'Expected: https://www.figma.com/design/<key>/... or a raw file key'
  )
}

// ── Token Loading ───────────────────────────────────────────────────

function loadFigmaToken(): string {
  if (process.env.FIGMA_TOKEN) return process.env.FIGMA_TOKEN

  const envPaths = [
    resolve(__dirname, '../../../.env.figma'),
    resolve(__dirname, '../.env'),
  ]

  for (const envPath of envPaths) {
    if (existsSync(envPath)) {
      const content = readFileSync(envPath, 'utf-8')
      const match = content.match(/^FIGMA_TOKEN=(.+)$/m)
      if (match && match[1].trim() && !match[1].startsWith('#')) {
        return match[1].trim()
      }
    }
  }

  throw new Error(
    'FIGMA_TOKEN not found.\n\n' +
    'Set it via:\n' +
    '  export FIGMA_TOKEN=figd_xxx\n' +
    '  or add to .env.figma in monorepo root\n\n' +
    'Get your token at: https://www.figma.com/settings'
  )
}

// ── Figma API ───────────────────────────────────────────────────────

async function fetchFileMetadata(fileKey: string, token: string): Promise<{ name: string }> {
  const response = await fetch(`https://api.figma.com/v1/files/${fileKey}?depth=1`, {
    headers: { 'X-Figma-Token': token },
  })

  if (!response.ok) {
    const text = await response.text()
    throw new Error(apiErrorMessage(response.status, text))
  }

  const data = await response.json() as { name: string }
  return { name: data.name }
}

async function fetchVariables(fileKey: string, token: string): Promise<FigmaVariablesResponse> {
  const response = await fetch(`https://api.figma.com/v1/files/${fileKey}/variables/local`, {
    headers: { 'X-Figma-Token': token },
  })

  if (!response.ok) {
    const text = await response.text()
    throw new Error(apiErrorMessage(response.status, text))
  }

  return response.json() as Promise<FigmaVariablesResponse>
}

function apiErrorMessage(status: number, body: string): string {
  const messages: Record<number, string> = {
    401: 'Authentication failed. Check your FIGMA_TOKEN.',
    403: 'Access denied. Ensure your token has file:read scope and the Variables API is available on your plan (Professional+).',
    404: 'File not found. Check the URL.',
    429: 'Rate limited by Figma API. Wait a moment and try again.',
  }
  return messages[status] ?? `Figma API error ${status}: ${body.slice(0, 200)}`
}

// ── Transform ───────────────────────────────────────────────────────

/**
 * Map Figma FLOAT to the correct W3C DTCG type based on collection semantics.
 * Figma only has FLOAT/COLOR/STRING/BOOLEAN — DTCG is more specific.
 */
const COLLECTION_TYPE_MAP: Record<string, string> = {
  spacing: 'dimension',
  radius: 'dimension',
  stroke: 'dimension',
  breakpoints: 'dimension',
  opacity: 'number',
  typography: 'number',
}

function resolveDtcgType(figmaType: string, collectionName: string): string {
  const rawType = figmaType.toLowerCase()
  if (rawType === 'color') return 'color'
  if (rawType === 'boolean') return 'boolean'
  if (rawType === 'string') return 'string'
  // FLOAT → look up by collection name
  const colKey = collectionName.toLowerCase()
  return COLLECTION_TYPE_MAP[colKey] ?? 'number'
}

function rgbaToHex({ r, g, b, a = 1 }: { r: number; g: number; b: number; a?: number }): string {
  const toHex = (n: number): string => {
    const hex = Math.round(n * 255).toString(16).toUpperCase()
    return hex.length === 1 ? '0' + hex : hex
  }
  const hex = `#${toHex(r)}${toHex(g)}${toHex(b)}`
  return a < 1 ? `${hex}${toHex(a)}` : hex
}

function pickModeId(collection: FigmaVariableCollection): string {
  for (const preferred of PREFERRED_MODES) {
    const mode = collection.modes.find(m =>
      m.name.toLowerCase() === preferred.toLowerCase()
    )
    if (mode) return mode.modeId
  }
  return collection.defaultModeId
}

function transformValue(
  rawValue: FigmaVariableValue,
  variable: FigmaVariable,
  allVariables: Record<string, FigmaVariable>,
  allCollections: Record<string, FigmaVariableCollection>,
  collectionName: string,
): { value: string | number | boolean; type: string } {
  const type = resolveDtcgType(variable.resolvedType, collectionName)

  // Alias reference
  if (typeof rawValue === 'object' && rawValue !== null && 'type' in rawValue && rawValue.type === 'VARIABLE_ALIAS') {
    const aliasVar = allVariables[rawValue.id]
    if (aliasVar) {
      const aliasCollection = allCollections[aliasVar.variableCollectionId]
      const collectionKey = sanitizeFileName(aliasCollection?.name ?? 'unknown')
      const aliasPath = aliasVar.name
        .split('/')
        .map((p: string) => p.toLowerCase().replace(/\s+/g, '-'))
        .join('.')
      return { value: `{figma.${collectionKey}.${aliasPath}}`, type }
    }
    return { value: `{alias:${rawValue.id}}`, type }
  }

  // Color
  if (type === 'color' && typeof rawValue === 'object' && rawValue !== null && 'r' in rawValue) {
    return { value: rgbaToHex(rawValue as { r: number; g: number; b: number; a?: number }), type }
  }

  return { value: rawValue as string | number | boolean, type }
}

function transformCollection(
  figmaData: FigmaVariablesResponse,
  collectionId: string,
): Record<string, unknown> {
  const { variables, variableCollections } = figmaData.meta
  const collection = variableCollections[collectionId]
  const modeId = pickModeId(collection)
  const tokens: Record<string, unknown> = {}

  for (const varId of collection.variableIds) {
    const variable = variables[varId]
    if (!variable || variable.hiddenFromPublishing) continue

    const rawValue = variable.valuesByMode[modeId]
    if (rawValue === undefined) continue

    const path = variable.name
      .split('/')
      .map((part: string) => part.toLowerCase().replace(/\s+/g, '-'))

    const { value, type } = transformValue(rawValue, variable, variables, variableCollections, collection.name)

    const tokenData: Record<string, unknown> = { $type: type, $value: value }
    if (variable.description) tokenData.$description = variable.description

    // Build nested structure
    let current: Record<string, unknown> = tokens
    for (let i = 0; i < path.length; i++) {
      if (i === path.length - 1) {
        current[path[i]] = tokenData
      } else {
        if (!current[path[i]] || typeof current[path[i]] !== 'object') {
          current[path[i]] = {}
        }
        current = current[path[i]] as Record<string, unknown>
      }
    }
  }

  return tokens
}

// ── MCP Transform ───────────────────────────────────────────────────

function transformMcpCollection(collection: McpCollection): Record<string, unknown> {
  const tokens: Record<string, unknown> = {}

  for (const variable of collection.variables) {
    const path = variable.name
      .split('/')
      .map((part: string) => part.toLowerCase().replace(/\s+/g, '-'))

    const type = resolveDtcgType(variable.type, collection.name)

    // Prefer alias reference; fall back to resolved value
    const value = variable.alias
      ? variable.alias.replace(/\{([^}]+)\}/g, (_: string, inner: string) =>
          `{${inner.split('.').map((s: string) => s.toLowerCase().replace(/\s+/g, '-')).join('.')}}`)
      : variable.value

    const tokenData: Record<string, unknown> = { $type: type, $value: value }

    // Build nested structure
    let current: Record<string, unknown> = tokens
    for (let i = 0; i < path.length; i++) {
      if (i === path.length - 1) {
        current[path[i]] = tokenData
      } else {
        if (!current[path[i]] || typeof current[path[i]] !== 'object') {
          current[path[i]] = {}
        }
        current = current[path[i]] as Record<string, unknown>
      }
    }
  }

  return tokens
}

// ── Merge (from sync-from-clipboard.js) ─────────────────────────────

function sanitizeFileName(name: string): string {
  return name
    .toLowerCase()
    .replace(/[\s/]+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '')
}

function countTokens(obj: Record<string, unknown>): number {
  let count = 0
  for (const [key, value] of Object.entries(obj)) {
    if (key.startsWith('$')) continue
    if (typeof value === 'object' && value !== null) {
      if ('$value' in value) count++
      else count += countTokens(value as Record<string, unknown>)
    }
  }
  return count
}

function mergeTokens(
  existing: Record<string, unknown>,
  incoming: Record<string, unknown>,
  path: string[] = [],
): { result: Record<string, unknown>; changes: MergeChange[] } {
  const result = { ...existing }
  const changes: MergeChange[] = []

  for (const [key, incomingValue] of Object.entries(incoming)) {
    const currentPath = [...path, key]
    const pathStr = currentPath.join('.')

    if (!(key in result)) {
      result[key] = incomingValue
      if (typeof incomingValue === 'object' && incomingValue !== null && '$value' in incomingValue) {
        changes.push({ path: pathStr, type: 'added', newValue: String((incomingValue as Record<string, unknown>).$value) })
      } else if (typeof incomingValue === 'object' && incomingValue !== null) {
        collectLeaves(incomingValue as Record<string, unknown>, currentPath).forEach(leaf => {
          changes.push({ path: leaf.path, type: 'added', newValue: String(leaf.value) })
        })
      }
      continue
    }

    const existingValue = result[key]

    if (
      typeof incomingValue === 'object' && incomingValue !== null &&
      typeof existingValue === 'object' && existingValue !== null &&
      '$value' in incomingValue && '$value' in existingValue
    ) {
      const inc = incomingValue as Record<string, unknown>
      const ext = existingValue as Record<string, unknown>
      if (String(inc.$value) !== String(ext.$value)) {
        changes.push({ path: pathStr, type: 'updated', oldValue: String(ext.$value), newValue: String(inc.$value) })
        result[key] = { ...ext, ...inc }
      }
      continue
    }

    if (
      typeof incomingValue === 'object' && incomingValue !== null &&
      typeof existingValue === 'object' && existingValue !== null &&
      !('$value' in incomingValue) && !('$value' in existingValue)
    ) {
      const nested = mergeTokens(
        existingValue as Record<string, unknown>,
        incomingValue as Record<string, unknown>,
        currentPath,
      )
      result[key] = nested.result
      changes.push(...nested.changes)
      continue
    }

    if (JSON.stringify(existingValue) !== JSON.stringify(incomingValue)) {
      changes.push({ path: pathStr, type: 'updated', oldValue: JSON.stringify(existingValue), newValue: JSON.stringify(incomingValue) })
      result[key] = incomingValue
    }
  }

  return { result, changes }
}

function collectLeaves(obj: Record<string, unknown>, basePath: string[] = []): Array<{ path: string; value: unknown }> {
  const leaves: Array<{ path: string; value: unknown }> = []
  for (const [key, value] of Object.entries(obj)) {
    if (key.startsWith('$')) continue
    const p = [...basePath, key]
    if (typeof value === 'object' && value !== null) {
      if ('$value' in value) {
        leaves.push({ path: p.join('.'), value: (value as Record<string, unknown>).$value })
      } else {
        leaves.push(...collectLeaves(value as Record<string, unknown>, p))
      }
    }
  }
  return leaves
}

function syncCollection(collectionName: string, tokens: Record<string, unknown>, targetDir: string): CollectionResult {
  const fileName = `${sanitizeFileName(collectionName)}.json`
  const filePath = resolve(targetDir, fileName)

  if (!existsSync(targetDir)) {
    mkdirSync(targetDir, { recursive: true })
  }

  let existing: Record<string, unknown> = {}
  let isNewFile = true

  if (existsSync(filePath)) {
    try {
      existing = JSON.parse(readFileSync(filePath, 'utf-8'))
      isNewFile = false
    } catch {
      // Corrupted file — treat as new
    }
  }

  const { result: merged, changes } = mergeTokens(existing, tokens)

  return {
    collectionName,
    fileName,
    filePath,
    isNewFile,
    merged,
    changes,
    tokenCount: countTokens(tokens),
  }
}

