const args = process.argv.slice(2)

if (args.includes('--help') || args.includes('-h') || args.length === 0) {
  printHelp()
  process.exit(0)
}

// Parse flags
const dataIdx = args.indexOf('--data')
const dataPath = dataIdx !== -1 ? args[dataIdx + 1] : undefined
const url = args.find(a => !a.startsWith('--') && a !== dataPath)
const dryRun = args.includes('--dry-run')
const skipConfirm = args.includes('--yes')
const enableBackup = args.includes('--backup')

if (!url && !dataPath) {
  console.error('Error: Figma URL or --data <path> required.\n')
  printHelp()
  process.exit(1)
}

const isTTY = process.stdin.isTTY ?? false
const effectiveSkipConfirm = skipConfirm || !isTTY
const renderOptions = !isTTY ? { stdin: new PassThrough() as any } : {}
render(<App url={url} dryRun={dryRun} skipConfirm={effectiveSkipConfirm} enableBackup={enableBackup} dataPath={dataPath} />, renderOptions)
