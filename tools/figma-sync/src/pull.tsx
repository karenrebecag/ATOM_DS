/**
 * ATOM Figma Pull — Fetch tokens from any Figma file
 *
 * Usage:
 *   pnpm figma:pull <url>                               Pull via REST API (Enterprise)
 *   pnpm figma:pull --data <path.json>                  Pull from pre-extracted MCP data
 *   pnpm figma:pull --data <path.json> --backup         Create backup before writing
 *   pnpm figma:pull --data <path.json> --dry-run        Preview changes without writing
 *   pnpm figma:pull --data <path.json> --yes            Skip confirmation prompt
 *   pnpm figma:pull --data <path.json> --backup --yes   Backup + auto-confirm
 *
 * The --data flag accepts a JSON file generated by Claude Code via
 * Figma MCP (use_figma Plugin API), bypassing the REST API entirely.
 * This works on ALL Figma plans — no Enterprise required.
 */

