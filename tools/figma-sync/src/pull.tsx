// ── Main App ────────────────────────────────────────────────────────

function App({ url, dryRun, skipConfirm, enableBackup, dataPath }: {
  url?: string
  dryRun: boolean
  skipConfirm: boolean
  enableBackup: boolean
  dataPath?: string
}): React.ReactElement {
  const { exit } = useApp()
  const [phase, setPhase] = useState<AppPhase>({ step: 'fetching', message: 'Loading...' })
  const [folderName, setFolderName] = useState('')
  const [collectionsData, setCollectionsData] = useState<CollectionResult[]>([])

  useEffect(() => {
    runPull().catch((err: Error) => {
      setPhase({ step: 'error', message: err.message })
    })
  }, [])

  async function runPull(): Promise<void> {
    let fileName: string
    let fileKey: string
    let results: CollectionResult[]

    if (dataPath) {
      // ── MCP mode: read pre-extracted data ──
      const pulled = await runMcpPull(dataPath)
      fileName = pulled.fileName
      fileKey = 'mcp'
      results = pulled.results
    } else if (url) {
      // ── REST API mode ──
      const pulled = await runRestPull(url)
      fileName = pulled.fileName
      fileKey = pulled.fileKey
      results = pulled.results
    } else {
      throw new Error('Either a Figma URL or --data <path> is required.')
    }

    const folder = sanitizeFileName(fileName)
    setFolderName(folder)
    setCollectionsData(results)

    const totalAdded = results.reduce((sum, r) => sum + r.changes.filter(c => c.type === 'added').length, 0)
    const totalUpdated = results.reduce((sum, r) => sum + r.changes.filter(c => c.type === 'updated').length, 0)

    if (totalAdded + totalUpdated === 0 && !results.some(r => r.isNewFile)) {
      setPhase({ step: 'summary', fileName, fileKey, collections: results, totalAdded: 0, totalUpdated: 0 })
      setTimeout(() => exit(), 100)
      return
    }

    setPhase({ step: 'summary', fileName, fileKey, collections: results, totalAdded, totalUpdated })

    if (dryRun) {
      setTimeout(() => exit(), 100)
      return
    }

    if (skipConfirm) {
      writeFiles(results, folder)
      return
    }

    setPhase({ step: 'confirm', collections: results })
  }

  async function runMcpPull(path: string): Promise<{ fileName: string; results: CollectionResult[] }> {
    setPhase({ step: 'fetching', message: 'Reading MCP data...' })

    const raw = readFileSync(path, 'utf-8')
    const mcpData: McpData = JSON.parse(raw)
    const fileName = mcpData.fileName

    const varCount = mcpData.collections.reduce((s, c) => s + c.variables.length, 0)
    setPhase({ step: 'fetching', message: `Transforming ${varCount} variables from ${mcpData.collections.length} collections...` })

    const folder = sanitizeFileName(fileName)
    const targetDir = resolve(TOKENS_BASE, folder)
    const results: CollectionResult[] = []

    for (const col of mcpData.collections) {
      const tokens = transformMcpCollection(col)
      if (Object.keys(tokens).length === 0) continue

      const collectionKey = sanitizeFileName(col.name)
      const wrappedTokens = { figma: { [collectionKey]: tokens } }
      const result = syncCollection(col.name, wrappedTokens, targetDir)
      results.push(result)
    }

    return { fileName, results }
  }

  async function runRestPull(inputUrl: string): Promise<{ fileName: string; fileKey: string; results: CollectionResult[] }> {
    const fileKey = parseFileKey(inputUrl)
    const token = loadFigmaToken()

    setPhase({ step: 'fetching', message: `Fetching "${fileKey}"...` })
    const { name: fileName } = await fetchFileMetadata(fileKey, token)

    setPhase({ step: 'fetching', message: `Fetching variables from "${fileName}"...` })
    const figmaData = await fetchVariables(fileKey, token)

    const varCount = Object.keys(figmaData.meta.variables).length
    const collectionCount = Object.keys(figmaData.meta.variableCollections).length

    if (varCount === 0) {
      throw new Error(`No variables found in "${fileName}". Is this a Variables-enabled file?`)
    }

    setPhase({ step: 'fetching', message: `Transforming ${varCount} variables from ${collectionCount} collections...` })

    const folder = sanitizeFileName(fileName)
    const targetDir = resolve(TOKENS_BASE, folder)
    const results: CollectionResult[] = []

    for (const [collectionId, collection] of Object.entries(figmaData.meta.variableCollections)) {
      const tokens = transformCollection(figmaData, collectionId)
      if (Object.keys(tokens).length === 0) continue

      const collectionKey = sanitizeFileName(collection.name)
      const wrappedTokens = { figma: { [collectionKey]: tokens } }
      const result = syncCollection(collection.name, wrappedTokens, targetDir)
      results.push(result)
    }

    return { fileName, fileKey, results }
  }

  function createBackup(results: CollectionResult[], folder: string): string | null {
    // Only backup files that will be modified (not new files)
    const filesToBackup = results.filter(r => existsSync(r.filePath) && (r.changes.length > 0 || r.isNewFile))

    if (filesToBackup.length === 0) return null

    const timestamp = new Date().toISOString().replace(/[:.]/g, '-').slice(0, -5)
    const backupDir = resolve(TOKENS_BASE, folder, `.backup-${timestamp}`)

    mkdirSync(backupDir, { recursive: true })

    for (const r of filesToBackup) {
      if (existsSync(r.filePath)) {
        const backupPath = resolve(backupDir, r.fileName)
        copyFileSync(r.filePath, backupPath)
      }
    }

    return backupDir
  }

  function writeFiles(results: CollectionResult[], folder: string): void {
    setPhase({ step: 'writing' })

    // Create backup if enabled
    let backupPath: string | null = null
    if (enableBackup) {
      backupPath = createBackup(results, folder)
    }

    let written = 0
    for (const r of results) {
      if (r.changes.length === 0 && !r.isNewFile) continue
      writeFileSync(r.filePath, JSON.stringify(r.merged, null, 2) + '\n', 'utf-8')
      written++
    }

    let gitDiff = ''
    try {
      gitDiff = execSync(`git diff --stat ${resolve(TOKENS_BASE, folder)}`, {
        encoding: 'utf-8',
        cwd: resolve(__dirname, '../../..'),
      })
    } catch {
      // Not a git repo
    }

    setPhase({ step: 'done', written, gitDiff, backupPath: backupPath ?? undefined })
    setTimeout(() => exit(), 100)
  }

  function handleConfirm(): void {
    writeFiles(collectionsData, folderName)
  }

  function handleCancel(): void {
    setPhase({ step: 'error', message: 'Cancelled.' })
    setTimeout(() => exit(), 100)
  }

  // Exit on error after render
  useEffect(() => {
    if (phase.step === 'error') {
      setTimeout(() => exit(), 100)
    }
  }, [phase.step])

  return (
    <Box flexDirection="column" padding={1}>
      <Header />

      {phase.step === 'fetching' && <FetchingView message={phase.message} />}

      {phase.step === 'summary' && (
        <Box flexDirection="column">
          <SummaryView
            fileName={phase.fileName}
            fileKey={phase.fileKey}
            collections={phase.collections}
            totalAdded={phase.totalAdded}
            totalUpdated={phase.totalUpdated}
            folderName={folderName}
          />
          {phase.totalAdded + phase.totalUpdated === 0 && (
            <Box marginTop={1}>
              <Text color="green">All files are already up to date.</Text>
            </Box>
          )}
        </Box>
      )}

      {phase.step === 'confirm' && (
        <Box flexDirection="column">
          <SummaryView
            fileName=""
            fileKey=""
            collections={phase.collections}
            totalAdded={collectionsData.reduce((s, r) => s + r.changes.filter(c => c.type === 'added').length, 0)}
            totalUpdated={collectionsData.reduce((s, r) => s + r.changes.filter(c => c.type === 'updated').length, 0)}
            folderName={folderName}
          />
          <ConfirmPrompt onConfirm={handleConfirm} onCancel={handleCancel} />
        </Box>
      )}

      {phase.step === 'writing' && (
        <Box gap={1}>
          <Text color="yellow"><Spinner type="dots" /></Text>
          <Text>Writing files...</Text>
        </Box>
      )}

      {phase.step === 'done' && (
        <DoneView written={phase.written} gitDiff={phase.gitDiff} backupPath={phase.backupPath} folderName={folderName} />
      )}

      {phase.step === 'error' && <ErrorView message={phase.message} />}
    </Box>
  )
}

